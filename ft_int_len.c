#include "libft.h"

size_t		ft_int_len(int num)
{
	size_t len;

	len = 1;
	while (num >= 10)
	{
		if (num == -2147483648)
		{
			len = 10;
			break ;
		}
		num = num / 10;
		len++;
	}
	return (len);
}
